int main()
{
    int N;
    while (cin >> N)
    {
        int n, l = 0;
        vector<int> steps;
        steps.push_back(-1);
        for (int i = 0; i < N; ++i)
        {
            cin >> n;
            if (n > steps[l])
            {
                steps.push_back(n);
                l++;
            }
            else
            {
                for (int j = 1; j <= l; j++)
                {
                    if (steps[j] >= n)
                    {
                        steps[j] = n;
                        break;
                    }
                }
            }
        }

        cout << l << endl;
    };
    system("pause");
    return 0;
}